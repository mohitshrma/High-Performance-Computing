#define width 100 
#define height 72

unsigned char image[], results[width * height];

typedef struct arguments {
unsigned char *input;
unsigned char *output;
  int start;
  int stride;
} 
arguments_m;

void edges(unsigned char *image,unsigned char *results) {
  pthread_t m1, m2, m3, m4;

  arguments_m m1_arguments;
  m1_arguments.start = 0;
  m1_arguments.stride = 4;
  m1_arguments.input = image;
  m1_arguments.output = results;


  arguments_m m2_arguments;
  m2_arguments.start = 1;
  m2_arguments.stride = 4;
  m2_arguments.input = image;
  m2_arguments.output = results;

  arguments_m m3_arguments;
  m3_arguments.start = 2;
  m3_arguments.stride = 4;
  m3_arguments.input = image;
  m3_arguments.output = results;

  arguments_m m4_arguments;
  m4_arguments.start = 3;
  m4_arguments.stride = 4;
  m4_arguments.input = image;
  m4_arguments.output = results;

  void *detect_edges();
  
  pthread_create(&m1, NULL, detect_edges, &m1_arguments);
  pthread_create(&m2, NULL, detect_edges, &m2_arguments);
  pthread_create(&m3, NULL, detect_edges, &m3_arguments);
  pthread_create(&m4, NULL, detect_edges, &m4_arguments);

  pthread_join(m1, NULL);
  pthread_join(m2, NULL);
  pthread_join(m3, NULL);
  pthread_join(m4, NULL);
}



void *detect_edges(arguments_m *args) {
  int i;
  unsigned char *in = args->input;
  unsigned char *out =args->output;
  int n_pixels = width * height;

  for(i=args->start;i<n_pixels;i+=args->stride) {
    int x, y; // the pixel of interest
    int b, d, f, h; // the pixels adjacent to x,y used for the calculation
    int s; // the result of calculate
    
    y = i / width;
    x = i - (width * y);

    if (x == 0 || y == 0 || x == width - 1 || y == height - 1) {
      results[i] = 0;
    } else {
      b = i + width;
      d = i - 1;
      f = i + 1;
      h = i - width;

      s = (in[i] * 4) + (in[b] * -1) + (in[d] * -1) + (in[f] * -1)
          + (in[h] * -1);

      if (s > 0) { // if the result is positive this is an edge pixel
        out[i] = 255;
      } else {
        out[i] = 0;
      }
    }
  }
}

 int time_difference(struct timespec *start, struct timespec *finish, 
                    long long int *difference) {
  long long int ds =  finish->tv_sec - start->tv_sec; 
  long long int dn =  finish->tv_nsec - start->tv_nsec; 

  if(dn < 0 ) {
    ds--;
    dn += 1000000000; 
  } 
  *difference = ds * 1000000000 + dn;
  return !(*difference > 0);
}

void tidy_and_exit() {
  exit(0);
}

void sigint_callback(int signal_number){
  printf("\nInterrupt from keyboard\n");
  tidy_and_exit();
}

static void display() {
  glClear(GL_COLOR_BUFFER_BIT);
  glRasterPos4i(-1, -1, 0, 1);
  glDrawPixels(width, height, GL_LUMINANCE, GL_UNSIGNED_BYTE, image);
  glRasterPos4i(0, -1, 0, 1);
  glDrawPixels(width, height, GL_LUMINANCE, GL_UNSIGNED_BYTE, results);
  glFlush();
}

static void key_pressed(unsigned char key, int x, int y) {
  switch(key){
    case 27: // escape
      tidy_and_exit();
      break;
    default: 
      printf("\nPress escape to exit\n");
      break;
  }
}

int main(int argc, char **argv) {
  signal(SIGINT, sigint_callback);
 
  printf("image dimensions %dx%d\n", width, height);

 struct timespec start, finish;   
  long long int time_elapsed;

  clock_gettime(CLOCK_MONOTONIC, &start);
 
  edges(image, results);

  clock_gettime(CLOCK_MONOTONIC, &finish);
  time_difference(&start, &finish, &time_elapsed);
  printf("Time elapsed was %lldns or %0.9lfs\n", time_elapsed, 
         (time_elapsed/1.0e9)); 

  glutInit(&argc, argv);
  glutInitWindowSize(width * 2,height);
  glutInitDisplayMode(GLUT_SINGLE | GLUT_LUMINANCE);
      
  glutCreateWindow("6CS005 Image Progessing Courework");
  glutDisplayFunc(display);
  glutKeyboardFunc(key_pressed);
  glClearColor(0.0, 1.0, 0.0, 1.0); 

  glutMainLoop(); 

  tidy_and_exit();
  
  return 0;
}

